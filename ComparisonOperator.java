package Java_6_Operators;

public class ComparisonOperator {
	public static void main(String[] args) {
		int a = 4;
		int b = 5;
		System.out.println(a == b); // Equal To
		
		int c = 3;
		int d = 1;
		System.out.println(c != d); // Not Equal
		
		int e = 2;
		int f = 4;
		System.out.println(f > e); // Greater Than 
		
		int g = 5;
		int h = 3;
		System.out.println(g < h); // Less Than
		
		int i = 4;
		int j = 7;
		System.out.println(j >= i); // Greater Than and Equal To
		
		int k = 3;
		int l = 5;
		System.out.println(k <= l); // Less Than and Equal To	
		
	}
}
