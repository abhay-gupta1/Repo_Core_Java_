package Java_4_DataTypes;

public class DataTypes {
	public static void main(String[] args) {
		int myNum = 127;
		float myFloatNum = 4.99f;
		char myLetter = 'A';     /*The char data type is used to store a single character. 
		                           The character must be surrounded by single quotes, like 'A' or 'c':*/
		
		boolean myBool = false; /*Boolean values are mostly used for conditional testing, 
		                          which you will learn more about in a later chapter.*/
		
		String myText= "Hello"; /*The String data type is used to store a sequence of characters (text). 
		                           String values must be surrounded by double quotes:*/
		
		System.out.println(myNum);
		System.out.println(myFloatNum);
		System.out.println(myLetter);
		System.out.println(myBool);
		System.out.println(myText);
	}
}
