package Operators_7;

public class Unary_decrement_Operators {
	public static void main(String[] args) {
		
		int a = 10;
		int b = --a; // pre decrement
		
		System.out.println(a);
		System.out.println(b);
		
		int c = 10;
		
		int d = a--; // post decrement
		
		System.out.println(c);
		System.out.println(d);
		
		
		
	}
}
