package Java_4_DataTypes;

public class PrimitiveDataTypes {
	public static void main(String[] args) {
		/*Integer types stores whole numbers, positive or negative (such as 123 or -456), without decimals. 
		 * Valid types are byte, short, int and long. Which type you should use, depends on the numeric value.*/
		
		byte myByte = 127;
		System.out.println(myByte);
		
		short myShort = 5000;
		System.out.println(myShort);
		
		int myInt = 234;
		System.out.println(myInt);
		
		long myLong = 234532453245L;
		System.out.println(myLong);
		
		/*Floating point types represents numbers with
		 a fractional part, containing one or more decimals. There are two types: float and double.*/
		
		float myFloat = 12.234328f;
		System.out.println(myFloat);
		
		double myDouble = 3.3987689765789654d;
		System.out.println(myDouble);
	}
}
