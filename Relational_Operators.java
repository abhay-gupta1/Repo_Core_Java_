package Operators_7;

public class Relational_Operators {
	public static void main(String[] args) {
		
		int A = 10;
		//int B = 5;
		int B = 10;
		
		System.out.println(A == B);  // Equal To Equal To Operator (==)
		
		System.out.println(A != B); // Not Equal To Operator(in equality)
		
		System.out.println(A > B); // Greater than Operator
		
		System.out.println(A < B); // Less than Operator
		
		System.out.println(A >= B); // Greater Than Operator
		
		System.out.println(A <= B); // Less Than Operator
	}
}
