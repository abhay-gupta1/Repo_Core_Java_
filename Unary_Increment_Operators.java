package Operators_7;

public class Unary_Increment_Operators {
	public static void main(String[] args) {
		
		int a = 10;
		
		int b = ++a; // pre increment
		
		System.out.println(a);
		System.out.println(b);
		
		int c = 10;
		
		int d = c++; // post increment
		
		System.out.println(c);
		System.out.println(d);
	}
}
