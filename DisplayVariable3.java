package Java_3_Variables;
/* 11. For numeric values, the + character works as a mathematical operator (notice that we use int (integer) variables here):
 * 
 */

public class DisplayVariable3 {
	public static void main(String[] args) {
		int x = 10;
		int y = 24;
		System.out.println(x + y);  // Print the value of x + y
	}
}
