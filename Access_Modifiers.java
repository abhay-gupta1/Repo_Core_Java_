package Java_24_Modifiers;
/*The public keyword is an access modifier, meaning that it is used to set the access level for classes,
 *  attributes, methods and constructors.

We divide modifiers into two groups:

Access Modifiers - controls the access level
Non-Access Modifiers - do not control access level, but provides other functionality*/

/*For classes, you can use either public or default:

Modifier	Description	Try it
public	: The class is accessible by any other class	
default: 	The class is only accessible by classes in the same package. This is used when you don't specify a modifier. */

/*For attributes, methods and constructors, you can use the one of the following:

Modifier	Description	Try it
public	: The code is accessible for all classes	
private	: The code is only accessible within the declared class	
default: 	The code is only accessible in the same package. This is used when you don't specify a modifier. You will learn more about packages
 in the Packages chapter	*/

